#include "libft.h"

void	*ft_calloc(size_t n, size_t size)
{
	void	*ptr;

	ptr = malloc(n * size);
	if (ptr == NULL)
		return (0);
	else
		ft_memset(ptr, 0, size * n);
	return (ptr);
}
